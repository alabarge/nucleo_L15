void HardFault_Handler_C(uint32_t *hardfault_args, uint32_t lr_value) {

   volatile uint32_t r0;
   volatile uint32_t r1;
   volatile uint32_t r2;
   volatile uint32_t r3;
   volatile uint32_t r12;
   volatile uint32_t lr;
   volatile uint32_t pc;
   volatile uint32_t psr;
   volatile uint32_t cfsr;
   volatile uint32_t bus_fault_address;
   volatile uint32_t memmanage_fault_address;

    bus_fault_address       = SCB->BFAR;
	 memmanage_fault_address = SCB->MMFAR;
	 cfsr                    = SCB->CFSR;

    r0 = hardfault_args[ 0 ];
    r1 = hardfault_args[ 1 ];
    r2 = hardfault_args[ 2 ];
    r3 = hardfault_args[ 3 ];

    r12 = hardfault_args[ 4 ];
    lr = hardfault_args[ 5 ];
    pc = hardfault_args[ 6 ];
    psr = hardfault_args[ 7 ];
    
    xlprint("\n\n");
    xlprint("[HardFault] Exception\n\n");
    xlprint("- Stack Frame:\n\n");
    xlprint(" R0    = %08X\n", r0);
    xlprint(" R1    = %08X\n", r1);
    xlprint(" R2    = %08X\n", r2);
    xlprint(" R3    = %08X\n", r3);
    xlprint(" R12   = %08X\n", r12);
    xlprint(" LR    = %08X\n", lr);
    xlprint(" PC    = %08X\n", pc);
    xlprint(" PSR   = %08X\n", psr);
    xlprint("\n");
    xlprint("- FSR/FAR:\n\n");
    xlprint(" CFSR  = %08X\n", r0);
    xlprint(" HFSR  = %08X\n", r1);
    xlprint(" DFSR  = %08X\n", r2);
    xlprint(" AFSR  = %08X\n", r3);
    if (cfsr & 0x0080) xlprint(" MMFAR = %08X\n", memmanage_fault_address);
    if (cfsr & 0x8000) xlprint(" BFAR  = %08X\n", bus_fault_address);
    xlprint("\n");
    xlprint("- Misc:\n\n");
    xlprint(" LR/EXC_RETURN = %08X\n", lr_value);
    xlprint("\n\n");

    while(1);
    
}


